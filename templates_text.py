"""
–§—É–Ω–∫—Ü–∏—è create_template_event_message —Å–æ–∑–¥–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –æ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–∏. 
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏ –æ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–∏, user_id  –∏ username(fullname) –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, —Å–æ–∑–¥–∞–≤—à–µ–≥–æ –º–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ.
"""
def create_template_event_message(data_event: dict,
                                  user_id: str,
                                  username: str,
                                  chat_id: str,
                                  index: int) -> str:

    newline_char_yes = '\n ‚úÖ'
    newline_char_no = '\n ‚ùå'
    newline_char_doubt = '\n üí≠'

    text = f'<b>-----------------------------</b>\n\n' \
           f'‚ö°Ô∏è<b>–ù–∞–∑–≤–∞–Ω–∏–µ –º–µ—Ä–æ–ø—Ä–∏—Ç–∏—è:</b>     {data_event[user_id][index][chat_id]["1"]["event_name"]}\n' \
           f'üóì   <b>–î–∞—Ç–∞ –ø—Ä–æ–≤–µ–¥–µ–Ω–∏—è:</b>     {data_event[user_id][index][chat_id]["2"]["date_of_the_event"]}\n' \
           f'‚è∞   <b>–í—Ä–µ–º—è –ø—Ä–æ–≤–µ–¥–µ–Ω–∏—è:</b>     {data_event[user_id][index][chat_id]["3"]["time_of_the_event"]}\n' \
           f'üóæ   <b>–ú–µ—Å—Ç–æ –ø—Ä–æ–≤–µ–¥–µ–Ω–∏—è:</b> {data_event[user_id][index][chat_id]["4"]["location"]}\n' \
           f'üíµ   <b>–í–∑–Ω–æ—Å:</b>  {data_event[user_id][index][chat_id]["5"]["payment"]}\n' \
           f'üë§   <b>–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—á–∞—Å—Ç–Ω–∏–∫–æ–≤:</b>    {data_event[user_id][index][chat_id]["6"]["count"]}\n\n' \
           '<b>-----------------------------</b>\n\n' \
           '–ò–¥—É—Ç:\n' \
           f'‚úÖ{newline_char_yes.join(data_event[user_id][index][chat_id]["yes"])}\n' \
           '<b>-----------------------------</b>\n\n' \
           '–ù–µ –∏–¥—É—Ç:\n' \
           f'‚ùå{newline_char_no.join(data_event[user_id][index][chat_id]["no"])}\n' \
           '<b>-----------------------------</b>\n\n' \
           '–°–æ–º–Ω–µ–≤–∞—é—Ç—Å—è:\n' \
           f'üí≠{newline_char_doubt.join(data_event[user_id][index][chat_id]["doubt"])}\n' \
           '<b>-----------------------------</b>\n\n' \
           '–°—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞:\n' \
           f'‚úÖ: <b>{len(data_event[user_id][index][chat_id]["yes"])}</b>\n' \
           f'‚ùå: <b>{len(data_event[user_id][index][chat_id]["no"])}</b>\n' \
           f'üí≠: <b>{len(data_event[user_id][index][chat_id]["doubt"])}</b>\n' \
           f'–í—Å–µ–≥–æ: <b>{len(data_event[user_id][index][chat_id]["yes"])+len(data_event[user_id][index][chat_id]["no"])+len(data_event[user_id][index][chat_id]["doubt"])}</b>\n' \
           '<b>-----------------------------</b>\n\n' \
           f'–ú–µ—Ä–æ–ø—Ä–∏—è—Ç–∏–µ —Å–æ–∑–¥–∞–ª: <b>{username}</b>\n' \
           f'ID: <b>{user_id}</b>\n' \
           '<b>-----------------------------</b>\n\n' \

    return text



